add_missing_class = dataset_config.add_missing_class

input_file = dataset_config.caption_input_file
output_file = dataset_config.caption_output_file.replace(
    dataset_config.name, f'{dataset_config.name}-{model_config.name}'
)
pre_path = output_file[:output_file.rfind('/')]
